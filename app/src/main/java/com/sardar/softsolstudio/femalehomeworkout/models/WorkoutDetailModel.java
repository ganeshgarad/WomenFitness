package com.sardar.softsolstudio.femalehomeworkout.models;

public class WorkoutDetailModel {
    private int id;
    private String calories;
    private String displayName;
    private String turns;
    private String name;
    private String url;
    private String info;
    private String howtodo;
    private String correctForm;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCalories() {
        return calories;
    }

    public void setCalories(String calories) {
        this.calories = calories;
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public String getTurns() {
        return turns;
    }

    public void setTurns(String turns) {
        this.turns = turns;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getHowtodo() {
        return howtodo;
    }

    public void setHowtodo(String howtodo) {
        this.howtodo = howtodo;
    }

    public String getCorrectForm() {
        return correctForm;
    }

    public void setCorrectForm(String correctForm) {
        this.correctForm = correctForm;
    }
}
